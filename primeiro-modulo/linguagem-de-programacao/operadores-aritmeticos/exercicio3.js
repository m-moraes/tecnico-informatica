/* Elaborar um programa que leia 2 notas de um aluno em
uma disciplina. Calcule e informe a média das notas. */

var primeiraNota = Number(prompt("Digite a primeira nota: "));
var segundaNota = Number(prompt("Digite a segunda nota: "));
var media = (primeiraNota + segundaNota) / 2

alert("A média final é de: " + media);